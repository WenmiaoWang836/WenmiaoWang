# 增强模型结构
class OfflineBERT(nn.Module):
    def __init__(self, vocab_size, embed_dim=256, num_classes=3):
        super().__init__()
        self.embedding = nn.Embedding(vocab_size, embed_dim)
        # 增加dropout防过拟合，加深网络
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=embed_dim, nhead=8, batch_first=True, dropout=0.2, activation="relu"
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=4)
        self.dropout = nn.Dropout(0.25)
        self.fc = nn.Linear(embed_dim, num_classes)

    def forward(self, x):
        x = self.embedding(x)
        x = self.transformer(x)
        x = x[:, 0, :]
        x = self.dropout(x)
        return self.fc(x)

# 字符分词器
class SimpleTokenizer:
    def __init__(self, max_len=32):
        self.max_len = max_len
        self.vocab = {"<PAD>":0, "<UNK>":1}

    def build_vocab(self, texts):
        for t in texts:
            for c in t:
                if c not in self.vocab:
                    self.vocab[c] = len(self.vocab)

    def encode(self, text):
        ids = [self.vocab.get(c, 1) for c in text[:self.max_len]]
        ids += [0] * (self.max_len - len(ids))
        return ids

# 数据集类
class DanmuDataset(Dataset):
    def __init__(self, encodings, labels):
        self.encodings = encodings
        self.labels = labels
    def __len__(self):
        return len(self.encodings)
    def __getitem__(self, idx):
        return torch.tensor(self.encodings[idx]), torch.tensor(self.labels[idx])

